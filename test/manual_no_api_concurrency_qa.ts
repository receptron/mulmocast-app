/**
 * Read the value of a number input whose <Label> matches the given text,
 * scoped to the Concurrency section (h4 = "並列実行設定" / "Concurrency").
 *
 * Returns null if the input is empty (concurrency unset), the number otherwise.
 */
async function readConcurrencyInputInProject(page: Page, labelText: string): Promise<number | "" | null> {
  return page.evaluate((label) => {
    // Locate the Concurrency card by h4 text
    const h4s = document.querySelectorAll("h4");
    let card: HTMLElement | null = null;
    for (const el of h4s) {
      const text = el.textContent?.trim() || "";
      if (text === "並列実行設定" || text === "Concurrency") {
        card = el.closest(".p-4, [class*='card']") as HTMLElement | null;
        if (card) break;
      }
    }
    if (!card) return null;

    // Find the label inside the card whose text matches and the sibling/descendant input
    const labels = card.querySelectorAll("label");
    for (const lbl of labels) {
      const t = lbl.textContent?.trim() || "";
      if (t.includes(label)) {
        // Walk up to find the wrapper div, then look for an <input type="number">
        let el: Element | null = lbl.parentElement;
        for (let depth = 0; el && depth < 3; depth++) {
          const input = el.querySelector("input[type='number']") as HTMLInputElement | null;
          if (input) {
            const value = input.value;
            if (value === "") return "";
            const num = Number(value);
            return Number.isFinite(num) ? num : null;
          }
          el = el.parentElement;
        }
      }
    }
    return null;
  }, labelText);
}

/**
 * Set the value of a number input by its label text inside the Concurrency card.
 * Empty string clears the input.
 */
async function setConcurrencyInputInProject(page: Page, labelText: string, value: string): Promise<boolean> {
  return page.evaluate(
    ({ label, val }) => {
      const h4s = document.querySelectorAll("h4");
      let card: HTMLElement | null = null;
      for (const el of h4s) {
        const text = el.textContent?.trim() || "";
        if (text === "並列実行設定" || text === "Concurrency") {
          card = el.closest(".p-4, [class*='card']") as HTMLElement | null;
          if (card) break;
        }
      }
      if (!card) return false;
      const labels = card.querySelectorAll("label");
      for (const lbl of labels) {
        const t = lbl.textContent?.trim() || "";
        if (t.includes(label)) {
          let el: Element | null = lbl.parentElement;
          for (let depth = 0; el && depth < 3; depth++) {
            const input = el.querySelector("input[type='number']") as HTMLInputElement | null;
            if (input) {
              const setter = Object.getOwnPropertyDescriptor(window.HTMLInputElement.prototype, "value")?.set;
              if (setter) {
                setter.call(input, val);
                input.dispatchEvent(new Event("input", { bubbles: true }));
                input.dispatchEvent(new Event("change", { bubbles: true }));
                return true;
              }
            }
            el = el.parentElement;
          }
        }
      }
      return false;
    },
    { label: labelText, val: value },
  );
}

