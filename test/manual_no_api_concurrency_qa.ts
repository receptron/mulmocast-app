async function readEditorJson(page: Page): Promise<Record<string, unknown> | null> {
  const viewLines = await page.$(".monaco-editor .view-lines");
  if (!viewLines) return null;
  await viewLines.click();
  await page.waitForTimeout(300);
  await page.keyboard.press(selectAllKey);
  await page.waitForTimeout(200);
  await page.keyboard.press(copyKey);
  await page.waitForTimeout(200);
  const text = await page.evaluate(() =>
    (
      window as unknown as { electronAPI: { readClipboardText: () => Promise<string> } }
    ).electronAPI.readClipboardText(),
  );
  try {
    return JSON.parse(text);
  } catch {
    return null;
  }
}

async function writeEditorJson(page: Page, json: Record<string, unknown>): Promise<boolean> {
  const viewLines = await page.$(".monaco-editor .view-lines");
  if (!viewLines) return false;
  await viewLines.click();
  await page.waitForTimeout(300);
  await page.keyboard.press(selectAllKey);
  await page.waitForTimeout(200);
  const newText = JSON.stringify(json, null, 2);
  await page.evaluate(
    (t) =>
      (
        window as unknown as { electronAPI: { writeClipboardText: (s: string) => Promise<void> } }
      ).electronAPI.writeClipboardText(t),
    newText,
  );
  await page.waitForTimeout(200);
  await page.keyboard.press(pasteKey);
  // Wait for Monaco internal ops before any tab switch may dispose the editor
  await page.waitForTimeout(2000);
  return true;
}

/** Concurrency-aware view of script paramsfor assertions */
type ConcurrencyView = {
  imageConcurrency: unknown;
  movieConcurrency: unknown;
  audioConcurrency: unknown;
};

function pickConcurrency(json: Record<string, unknown> | null): ConcurrencyView {
  const get = (key: string): unknown => {
    const block = json?.[key] as Record<string, unknown> | undefined;
    return block?.concurrency;
  };
  return {
    imageConcurrency: get("imageParams"),
    movieConcurrency: get("movieParams"),
    audioConcurrency: get("audioParams"),
  };
}

async function readConcurrencyFromJsonTab(page: Page): Promise<ConcurrencyView | null> {
  if (!(await navigateToJsonTab(page))) return null;
  const json = await readEditorJson(page);
  if (!json) return null;
  return pickConcurrency(json);
}

