async function testJsonToUI(page: Page) {
  console.log("\n=== 6. JSON → UI round-trip ===");

  // Use the INJECT project
  if (!(await openProjectByTitle(page, TITLE_INJECT))) {
    record("Open project (json→ui)", "FAIL", `Project not found: ${TITLE_INJECT}`);
    return;
  }

  if (!(await navigateToJsonTab(page))) {
    record("Navigate JSON tab (json→ui)", "FAIL", "JSON tab not found");
    return;
  }

  const json = await readEditorJson(page);
  if (!json) {
    record("Read JSON (json→ui)", "FAIL", "Could not read");
    return;
  }

  const FROM_JSON_IMAGE_MOVIE = 13;
  const FROM_JSON_AUDIO = 17;

  json.imageParams = { ...((json.imageParams as Record<string, unknown>) ?? {}), concurrency: FROM_JSON_IMAGE_MOVIE };
  json.movieParams = { ...((json.movieParams as Record<string, unknown>) ?? {}), concurrency: FROM_JSON_IMAGE_MOVIE };
  json.audioParams = { ...((json.audioParams as Record<string, unknown>) ?? {}), concurrency: FROM_JSON_AUDIO };

  const wrote = await writeEditorJson(page, json);
  record("Write concurrency via JSON", wrote ? "PASS" : "FAIL", wrote ? "OK" : "Failed");

  // Wait for Monaco to settle before leaving JSON tab
  await page.waitForTimeout(CONFIG.EDITOR_SETTLE_DELAY_MS);

  if (!(await navigateToStyleTab(page))) {
    record("Navigate Style tab (json→ui)", "FAIL", "Style tab not found");
    return;
  }
  await page.waitForTimeout(CONFIG.NAVIGATION_DELAY_MS);
  await scrollToConcurrencySection(page);

  const imgVal =
    (await readConcurrencyInputInProject(page, "画像")) ?? (await readConcurrencyInputInProject(page, "Image"));
  const audVal =
    (await readConcurrencyInputInProject(page, "音声")) ?? (await readConcurrencyInputInProject(page, "Audio"));
  record(
    "image/movie input reflects JSON",
    imgVal === FROM_JSON_IMAGE_MOVIE ? "PASS" : "FAIL",
    `Got ${JSON.stringify(imgVal)}, expected ${FROM_JSON_IMAGE_MOVIE}`,
  );
  record(
    "audio input reflects JSON",
    audVal === FROM_JSON_AUDIO ? "PASS" : "FAIL",
    `Got ${JSON.stringify(audVal)}, expected ${FROM_JSON_AUDIO}`,
  );
}

