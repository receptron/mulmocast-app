/**
 * Concurrency QA Test Suite (no API cost)
 *
 * Manual test script to verify the Concurrency feature added in
 * receptron/mulmocast-app#1618:
 *
 *   - Settings page: "Concurrency Defaults" card (image/movie + audio)
 *   - Project Style tab: "Concurrency" card (per-project override)
 *   - createProject: global defaults are injected once at creation time
 *     into script.imageParams / movieParams / audioParams .concurrency
 *   - Re-opening an existing project does NOT mutate it
 *
 *   Phase 1 – Settings
 *     1a. Section exists, fields are present, blank state
 *     1b. Set values, verify persistence (close & reopen modal)
 *
 *   Phase 2 – Inject on create (Settings set)
 *     2a. Create a new project with image/movie=A, audio=B → JSON has those
 *
 *   Phase 3 – Inject on create (Settings blank)
 *     3a. Clear Settings, create a new project → JSON has no concurrency fields
 *
 *   Phase 4 – Existing project untouched
 *     4a. Re-set Settings to non-blank, navigate away from the Phase 3 project
 *         and back → JSON still has no concurrency fields (no mutation on open)
 *
 *   Phase 5 – Style tab per-project edits
 *     5a. image/movie input writes the same value to imageParams + movieParams
 *     5b. audio input writes audioParams.concurrency
 *     5c. clearing inputs removes the concurrency fields from JSON
 *
 *   Phase 6 – JSON → UI round-trip
 *     6a. Editing JSON updates the Style tab inputs
 *
 *   Phase 7 – Console health
 *
 * Usage:
 *   1. Start the app: yarn start
 *   2. Run: npx tsx test/manual_no_api_concurrency_qa.ts
 *
 * Environment variables:
 *   CDP_URL  - CDP endpoint (default: http://localhost:9222/)
 *   APP_URL  - App URL fragment to match (default: localhost:5175)
 */
