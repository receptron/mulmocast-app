async function phaseHealth(monitor: ConsoleMonitor): Promise<void> {
  console.log("\n--- Phase 4: Health ---");

  const filteredErrors = monitor.errors.filter(
    (e) => !e.includes("Electron Security Warning") && !e.includes("net::ERR_"),
  );

  record(
    "Console errors",
    filteredErrors.length === 0 ? "PASS" : "FAIL",
    filteredErrors.length === 0 ? "No errors" : `${filteredErrors.length} error(s)`,
  );
  filteredErrors.forEach((e) => {
    console.log(`    ERROR: ${e.substring(0, 200)}`);
  });
}

