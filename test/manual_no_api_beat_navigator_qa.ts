const selectAllKey = process.platform === "darwin" ? "Meta+a" : "Control+a";
const pasteKey = process.platform === "darwin" ? "Meta+v" : "Control+v";
const copyKey = process.platform === "darwin" ? "Meta+c" : "Control+c";

async function clickTabByText(page: Page, tabText: string): Promise<boolean> {
  const tabs = await page.locator('[role="tab"]').all();
  for (const tab of tabs) {
    const text = (await tab.textContent())?.trim() || "";
    if (text === tabText || text.startsWith(tabText)) {
      await tab.click();
      await page.waitForTimeout(CONFIG.NAVIGATION_DELAY_MS);
      return true;
    }
  }
  return false;
}

async function navigateToBeatTab(page: Page): Promise<boolean> {
  const tabs = await page.locator('[role="tab"]').all();
  for (const tab of tabs) {
    const text = (await tab.textContent())?.trim() || "";
    if (text.startsWith("BEAT")) {
      await tab.click();
      await page.waitForTimeout(CONFIG.NAVIGATION_DELAY_MS);
      return true;
    }
  }
  return false;
}

async function navigateToTextTab(page: Page): Promise<boolean> {
  return (await clickTabByText(page, "Text")) || (await clickTabByText(page, "翻訳"));
}

type GenerationOptionKey = "movie" | "audio" | "pdfHandout";

async function setGenerationOption(page: Page, key: GenerationOptionKey, checked: boolean): Promise<boolean> {
  const checkbox = page.locator(`[data-testid="generate-option-${key}-checkbox"]`).first();
  if ((await checkbox.count()) === 0) return false;

  const isChecked = (await checkbox.getAttribute("data-state")) === "checked";
  if (isChecked !== checked) {
    await checkbox.click();
    await page.waitForTimeout(CONFIG.ACTION_DELAY_MS);
  }
  return true;
}

async function waitForDialogToClose(page: Page): Promise<boolean> {
  try {
    await page.waitForSelector('[role="dialog"]', {
      state: "hidden",
      timeout: CONFIG.DIALOG_CLOSE_DELAY_MS + 2000,
    });
    return true;
  } catch {
    return false;
  }
}

async function waitForBeatVisible(page: Page, beatId: string): Promise<boolean> {
  try {
    await page.waitForFunction(
      (id) => {
        const el = document.querySelector(`[data-beat-id="${id}"]`);
        const container = el?.closest(".overflow-y-auto");
        if (!el || !container) return false;
        const cRect = container.getBoundingClientRect();
        const eRect = el.getBoundingClientRect();
        return eRect.top >= cRect.top - 50 && eRect.top <= cRect.bottom + 50;
      },
      beatId,
      { timeout: CONFIG.DIALOG_CLOSE_DELAY_MS + 2500 },
    );
    return true;
  } catch {
    return false;
  }
}

