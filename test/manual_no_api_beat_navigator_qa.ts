async function phaseSetup(page: Page): Promise<boolean> {
  console.log("\n--- Phase 1: Setup ---");

  // Navigate to dashboard
  await page.goto(page.url().split("#")[0] + "#/");
  await page.waitForTimeout(CONFIG.NAVIGATION_DELAY_MS);

  // Close any overlay
  const overlay = await page.$('[data-state="open"][aria-hidden="true"]');
  if (overlay) {
    await page.keyboard.press("Escape");
    await page.waitForTimeout(CONFIG.ACTION_DELAY_MS);
  }

  // Create project
  const newBtn = await page.$('[data-testid="create-new-button"]');
  if (!newBtn) {
    record("Create project", "FAIL", "Create button not found");
    return false;
  }
  await newBtn.click();
  await page.waitForTimeout(CONFIG.PROJECT_CREATE_DELAY_MS);
  const created = page.url().includes("#/project/");
  record("Create project", created ? "PASS" : "FAIL", `URL: ${page.url()}`);
  if (!created) return false;

  // Write test script via JSON tab
  await clickTabByText(page, "JSON");
  await page.waitForTimeout(CONFIG.EDITOR_LOAD_DELAY_MS);

  const testScript = createTestScript();
  const written = await writeEditorJson(page, testScript);
  record("Write test script", written ? "PASS" : "FAIL", `${BEAT_COUNT} markdown beats`);
  if (!written) return false;

  // Verify beats were written
  const json = await readEditorJson(page);
  const beatCount = (json?.beats as unknown[])?.length ?? 0;
  record("Verify beats", beatCount === BEAT_COUNT ? "PASS" : "FAIL", `Expected ${BEAT_COUNT}, got ${beatCount}`);

  // Switch to BEAT tab and generate images via PDF Handout
  await page.waitForTimeout(CONFIG.EDITOR_SETTLE_DELAY_MS);
  await navigateToBeatTab(page);

  // Enable PDF Handout, disable Movie and Podcast for image-only generation
  const pdfCheckbox = await setGenerationOption(page, "pdfHandout", true);
  await setGenerationOption(page, "movie", false);
  await setGenerationOption(page, "audio", false);

  if (!pdfCheckbox) {
    record("Enable PDF Handout", "WARN", "PDF Handout checkbox not found, skipping image generation");
  } else {
    record("Enable PDF Handout", "PASS", "Enabled");

    // Click Generate Contents
    const generateBtn = await page.$('[data-testid="generate-contents-button"]');
    if (generateBtn) {
      await generateBtn.click();
      console.log("  ⏳ Generating images via PDF Handout...");

      // Wait for generation to complete (check for progress indicator disappearing)
      try {
        await page.waitForFunction(
          () => {
            const btn = document.querySelector('[data-testid="generate-contents-button"]');
            return btn && !btn.hasAttribute("disabled");
          },
          { timeout: CONFIG.IMAGE_GENERATION_TIMEOUT_MS },
        );

        const realThumbnailCount = await page.evaluate(() => {
          return Array.from(document.querySelectorAll<HTMLImageElement>("[data-beat-id] img")).filter((img) => {
            const src = img.getAttribute("src");
            return typeof src === "string" && src.trim().length > 0;
          }).length;
        });
        const generatedImagesOk = realThumbnailCount >= BEAT_COUNT;
        record(
          "Generate images",
          generatedImagesOk ? "PASS" : "FAIL",
          generatedImagesOk
            ? `PDF Handout generation complete (${realThumbnailCount} real thumbnails)`
            : `Expected >=${BEAT_COUNT} real thumbnails, got ${realThumbnailCount}`,
        );
      } catch {
        record("Generate images", "WARN", "Generation may still be in progress or timed out");
      }
    } else {
      record("Generate images", "WARN", "Generate button not found");
    }
  }

  // Restore defaults: disable PDF Handout, re-enable Movie (which auto-enables Podcast)
  await setGenerationOption(page, "pdfHandout", false);
  await setGenerationOption(page, "movie", true);

  return true;
}

