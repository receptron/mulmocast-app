(async () => {
  console.log(`\nBeat Navigator QA Test Suite`);
  console.log(`Run ID: ${runId}`);
  console.log(`${"=".repeat(50)}`);

  let browser: Browser | undefined;
  let exitCode = 0;

  try {
    browser = await connectCDP();
    const page = findAppPage(browser.contexts());
    const monitor = createConsoleMonitor(page);
    monitor.start();

    const setupOk = await phaseSetup(page);
    if (setupOk) {
      await phaseMediaTab(page);
      await phaseTextTab(page);
    }

    monitor.stop();
    await phaseHealth(monitor);
  } catch (error: unknown) {
    console.error("\n✗ Fatal error:", error instanceof Error ? error.message : String(error));
    exitCode = 1;
  } finally {
    if (browser) await browser.close();
  }

  // Summary
  const passed = results.filter((r) => r.status === "PASS").length;
  const failed = results.filter((r) => r.status === "FAIL").length;
  const warned = results.filter((r) => r.status === "WARN").length;

  console.log(`\n${"=".repeat(50)}`);
  console.log(`  Results: ${passed} PASS / ${failed} FAIL / ${warned} WARN`);
  console.log(`${"=".repeat(50)}`);

  if (failed > 0) {
    console.log("\nFailed tests:");
    results
      .filter((r) => r.status === "FAIL")
      .forEach((r) => {
        console.log(`  ✗ ${r.name}: ${r.detail}`);
      });
    exitCode = 1;
  }

  process.exit(exitCode);
})();
