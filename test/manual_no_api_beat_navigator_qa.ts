// Phase 2: Beat Navigator on MEDIA tab
// =====================================================================

async function phaseMediaTab(page: Page): Promise<void> {
  console.log("\n--- Phase 2: Beat Navigator - MEDIA tab ---");

  await navigateToBeatTab(page);
  await page.waitForTimeout(CONFIG.ACTION_DELAY_MS);

  // Test: Floating button exists
  const mediaBtn = await page.$('[data-testid="beat-navigator-button-media"]');
  record("Floating button (MEDIA)", mediaBtn ? "PASS" : "FAIL", mediaBtn ? "Found" : "Not found");
  if (!mediaBtn) return;

  // Test: Open dialog
  await mediaBtn.click();
  await page.waitForTimeout(CONFIG.ACTION_DELAY_MS);

  const dialog = await page.$('[role="dialog"]');
  record("Dialog opens", dialog ? "PASS" : "FAIL", dialog ? "Dialog visible" : "Dialog not found");
  if (!dialog) return;

  // Test: Correct number of beats in dialog
  const beatButtons = await dialog.$$('[data-testid^="beat-card-"]');
  record(
    "Dialog beat count",
    beatButtons.length === BEAT_COUNT ? "PASS" : "FAIL",
    `Expected ${BEAT_COUNT}, got ${beatButtons.length}`,
  );

  // Prepare dialog scroll viewport for wheel tests
  const mediaViewportReady = await dialog.evaluate((el) => {
    const candidates = Array.from(el.querySelectorAll<HTMLElement>("*")).filter(
      (node) => node.scrollHeight - node.clientHeight > 10,
    );
    if (candidates.length === 0) return false;
    const viewport = candidates.sort((a, b) => b.scrollHeight - b.clientHeight - (a.scrollHeight - a.clientHeight))[0];
    viewport.setAttribute("data-qa-dialog-scroll", "media");
    viewport.scrollTop = 0;
    return true;
  });

  // Test: Scroll inside dialog (down/up)
  const mediaDialogScroll = await dialog.evaluate((el) => {
    const candidates = Array.from(el.querySelectorAll<HTMLElement>("*")).filter(
      (node) => node.scrollHeight - node.clientHeight > 10,
    );
    if (candidates.length === 0) return null;

    const viewport = candidates.sort((a, b) => b.scrollHeight - b.clientHeight - (a.scrollHeight - a.clientHeight))[0];
    const before = viewport.scrollTop;
    const maxTop = Math.max(0, viewport.scrollHeight - viewport.clientHeight);
    viewport.scrollTop = Math.min(maxTop, before + Math.max(120, Math.floor(viewport.clientHeight * 0.8)));
    const afterDown = viewport.scrollTop;
    viewport.scrollTop = 0;
    const afterUp = viewport.scrollTop;
    return { before, afterDown, afterUp, maxTop };
  });

  record(
    "Dialog scroll down (MEDIA)",
    !!mediaDialogScroll && mediaDialogScroll.afterDown > mediaDialogScroll.before ? "PASS" : "FAIL",
    mediaDialogScroll
      ? `scrollTop: ${mediaDialogScroll.before} → ${mediaDialogScroll.afterDown}`
      : "No scrollable viewport found",
  );
  record(
    "Dialog scroll up (MEDIA)",
    !!mediaDialogScroll && mediaDialogScroll.afterUp === 0 ? "PASS" : "FAIL",
    mediaDialogScroll ? `scrollTop after up: ${mediaDialogScroll.afterUp}` : "No scrollable viewport found",
  );

  // Test: Mouse wheel scroll inside dialog (down/up)
  if (mediaViewportReady) {
    const viewport = page.locator('[data-qa-dialog-scroll="media"]').first();
    const box = await viewport.boundingBox();
    if (box) {
      await page.mouse.move(box.x + box.width / 2, box.y + Math.min(40, box.height / 2));
      const wheelBefore = await page.evaluate(() => {
        const el = document.querySelector('[data-qa-dialog-scroll="media"]') as HTMLElement | null;
        return el?.scrollTop ?? -1;
      });
      await page.mouse.wheel(0, 700);
      await page.waitForTimeout(200);
      const wheelAfterDown = await page.evaluate(() => {
        const el = document.querySelector('[data-qa-dialog-scroll="media"]') as HTMLElement | null;
        return el?.scrollTop ?? -1;
      });
      await page.mouse.wheel(0, -700);
      await page.waitForTimeout(200);
      const wheelAfterUp = await page.evaluate(() => {
        const el = document.querySelector('[data-qa-dialog-scroll="media"]') as HTMLElement | null;
        return el?.scrollTop ?? -1;
      });
      record(
        "Dialog wheel scroll down (MEDIA)",
        wheelAfterDown > wheelBefore ? "PASS" : "FAIL",
        `scrollTop: ${wheelBefore} → ${wheelAfterDown}`,
      );
      record(
        "Dialog wheel scroll up (MEDIA)",
        wheelAfterUp < wheelAfterDown ? "PASS" : "FAIL",
        `scrollTop: ${wheelAfterDown} → ${wheelAfterUp}`,
      );
    } else {
      record("Dialog wheel scroll down (MEDIA)", "FAIL", "Scrollable viewport not interactable");
      record("Dialog wheel scroll up (MEDIA)", "FAIL", "Scrollable viewport not interactable");
    }
  } else {
    record("Dialog wheel scroll down (MEDIA)", "FAIL", "No scrollable viewport found");
    record("Dialog wheel scroll up (MEDIA)", "FAIL", "No scrollable viewport found");
  }

  // Test: Each beat has a real thumbnail image
  const thumbnailCount = await dialog.evaluate((el) => {
    return Array.from(el.querySelectorAll("img")).filter((img) => {
      const src = img.getAttribute("src");
      return typeof src === "string" && src.trim().length > 0;
    }).length;
  });
  record("Thumbnails present", thumbnailCount >= BEAT_COUNT ? "PASS" : "FAIL", `Found ${thumbnailCount} real images`);
  if (thumbnailCount < BEAT_COUNT) return;

  // Test: Beat text is displayed
  const hasText = await dialog.evaluate((el, rid) => {
    const paragraphs = el.querySelectorAll("p");
    for (const p of paragraphs) {
      if (p.textContent?.includes(`run ${rid}`)) return true;
    }
    return false;
  }, runId.toString());
  record(
    "Beat text displayed",
    hasText ? "PASS" : "FAIL",
    hasText ? "Test text found in dialog" : "Test text not found",
  );

  // Test: Last beat card is clickable when dialog is scrolled to bottom
  await page.evaluate(() => {
    const el = document.querySelector('[data-qa-dialog-scroll="media"]') as HTMLElement | null;
    if (!el) return;
    el.scrollTop = el.scrollHeight - el.clientHeight;
  });
  await page.waitForTimeout(200);

  // First, record initial editor scroll position
  const scrollBefore = await page.evaluate(() => {
    const container = document
      .querySelector('[data-testid="beat-navigator-button-media"]')
      ?.closest(".overflow-y-auto");
    return container?.scrollTop ?? -1;
  });

  // Click last beat button from bottom of dialog
  if (beatButtons.length > 0) {
    const lastBeatBtn = beatButtons[beatButtons.length - 1];
    await lastBeatBtn.click();

    const dialogClosed = await waitForDialogToClose(page);
    if (!dialogClosed) {
      record("Dialog closes after selection (MEDIA last)", "FAIL", "Dialog did not close after selecting last beat");
      return;
    }

    const lastBeatId = `${runId}-beat-${BEAT_COUNT}`;
    await waitForBeatVisible(page, lastBeatId);

    const scrollAfter = await page.evaluate(() => {
      const container = document
        .querySelector('[data-testid="beat-navigator-button-media"]')
        ?.closest(".overflow-y-auto");
      return container?.scrollTop ?? -1;
    });

    const lastBeatVisible = await page.evaluate((beatId) => {
      const el = document.querySelector(`[data-beat-id="${beatId}"]`);
      const container = el?.closest(".overflow-y-auto");
      if (!el || !container) return false;
      const cRect = container.getBoundingClientRect();
      const eRect = el.getBoundingClientRect();
      return eRect.top >= cRect.top - 50 && eRect.top <= cRect.bottom + 50;
    }, lastBeatId);

    record(
      "Last beat clickable at dialog bottom (MEDIA)",
      lastBeatVisible ? "PASS" : scrollAfter !== scrollBefore ? "WARN" : "FAIL",
      `scrollTop: ${scrollBefore} → ${scrollAfter}, beat visible: ${lastBeatVisible}`,
    );
  }

  // Test: Navigate to beat 5
  const navBtn2 = await page.$('[data-testid="beat-navigator-button-media"]');
  if (navBtn2) {
    await navBtn2.click();
    await page.waitForTimeout(CONFIG.ACTION_DELAY_MS);

    const dialog2 = await page.$('[role="dialog"]');
    if (dialog2) {
      const beatButtons2 = await dialog2.$$('[data-testid^="beat-card-"]');
      const beat5Btn = beatButtons2[4];
      if (beat5Btn) {
        const scrollBeforeBeat5 = await page.evaluate(() => {
          const container = document
            .querySelector('[data-testid="beat-navigator-button-media"]')
            ?.closest(".overflow-y-auto");
          return container?.scrollTop ?? -1;
        });

        await beat5Btn.click();
        const dialogClosed = await waitForDialogToClose(page);
        if (!dialogClosed) {
          record("Dialog closes after selection (MEDIA beat5)", "FAIL", "Dialog did not close after selecting beat 5");
          return;
        }

        const beat5Id = `${runId}-beat-5`;
        await waitForBeatVisible(page, beat5Id);

        const scrollAfterBeat5 = await page.evaluate(() => {
          const container = document
            .querySelector('[data-testid="beat-navigator-button-media"]')
            ?.closest(".overflow-y-auto");
          return container?.scrollTop ?? -1;
        });

        const beat5Visible = await page.evaluate((beatId) => {
          const el = document.querySelector(`[data-beat-id="${beatId}"]`);
          const container = el?.closest(".overflow-y-auto");
          if (!el || !container) return false;
          const cRect = container.getBoundingClientRect();
          const eRect = el.getBoundingClientRect();
          return eRect.top >= cRect.top - 50 && eRect.top <= cRect.bottom + 50;
        }, beat5Id);

        record(
          "Scroll to beat 5 (MEDIA)",
          beat5Visible ? "PASS" : scrollAfterBeat5 !== scrollBeforeBeat5 ? "WARN" : "FAIL",
          `scrollTop: ${scrollBeforeBeat5} → ${scrollAfterBeat5}, beat visible: ${beat5Visible}`,
        );
      }
    }
  }

  // Test: Navigate to first beat (after beat 5)
  const navBtn3 = await page.$('[data-testid="beat-navigator-button-media"]');
  if (navBtn3) {
    await navBtn3.click();
    await page.waitForTimeout(CONFIG.ACTION_DELAY_MS);

    const dialog3 = await page.$('[role="dialog"]');
    if (dialog3) {
      const beatButtons3 = await dialog3.$$('[data-testid^="beat-card-"]');
      const firstBeatBtn = beatButtons3[0];
      if (firstBeatBtn) {
        await firstBeatBtn.click();
        const dialogClosed = await waitForDialogToClose(page);
        if (!dialogClosed) {
          record("Dialog closes after selection (MEDIA beat1)", "FAIL", "Dialog did not close after selecting beat 1");
          return;
        }

        const firstBeatId = `${runId}-beat-1`;
        await waitForBeatVisible(page, firstBeatId);

        const firstBeatVisible = await page.evaluate((beatId) => {
          const el = document.querySelector(`[data-beat-id="${beatId}"]`);
          const container = el?.closest(".overflow-y-auto");
          if (!el || !container) return false;
          const cRect = container.getBoundingClientRect();
          const eRect = el.getBoundingClientRect();
          return eRect.top >= cRect.top - 50 && eRect.top <= cRect.bottom + 50;
        }, firstBeatId);

        record(
          "Scroll to first beat (MEDIA)",
          firstBeatVisible ? "PASS" : "FAIL",
          `Beat 1 visible: ${firstBeatVisible}`,
        );
      }
    }
  }
}

